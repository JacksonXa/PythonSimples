import math

# 1 - Acessar o numero PI
print(math.pi)
print(f"{math.pi:.2f}")

# 2 - Acessar o numero Euler
print(math.e)
print(f"{math.e:.2f}")

# 3 - Arredondando numero para cima e para baixo
num = 10.4
print(math.ceil(num))
print(math.floor(num))

# 4 - Fatorial de um numero
num2 = int(input("Digite um numero:\n"))
print(math.factorial(num2))

# 5 - Potencia de numeros
print(math.pow(5, 5))

# 6 - Raiz quadrada
print(math.sqrt(169))

# 7 - MDC
print(math.gcd(20, 100))

# 8 - Logaritimo
print(math.log(10))
